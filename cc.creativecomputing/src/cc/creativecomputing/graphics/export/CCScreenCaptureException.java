package cc.creativecomputing.graphics.export;

public class CCScreenCaptureException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -5784843562165839840L;

	public CCScreenCaptureException() {
		super();
	}

	public CCScreenCaptureException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CCScreenCaptureException(String arg0) {
		super(arg0);
	}

	public CCScreenCaptureException(Throwable arg0) {
		super(arg0);
	}

	
}
