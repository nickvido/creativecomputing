package cc.creativecomputing.protocol.udp;

public class CCUDPException extends RuntimeException{

	public CCUDPException() {
		super();
	}

	public CCUDPException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CCUDPException(String arg0) {
		super(arg0);
	}

	public CCUDPException(Throwable arg0) {
		super(arg0);
	}

}
